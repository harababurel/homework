clear;
conflevel = input('confidence level [0; 1] = '); % 1 - alpha
alpha = 1 - conflevel;
X = [99.8 * ones(1,2), ...
     99.9 * ones(1,5), ...
     98.0 * ones(1,3), ...
     100.1 * ones(1,4), ...
     100.5 * ones(1,2), ...
     100.0 * ones(1,2), ...
     100.2 * ones(1,2)];
mx = mean(X);
n = length(X);
s = std(X);
t1 = tinv(1 - alpha/2, n-1);
t2 = tinv(alpha/2, n-1); % t2 = -t1
ci1 = mx - s/sqrt(n) * t1;
ci2 = mx - s/sqrt(n) * t2;

fprintf('C.I. for the mean (sigma unknown): (%3.4f, %3.4f)\n', ci1, ci2);